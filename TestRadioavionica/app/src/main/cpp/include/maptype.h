typedef struct FRAME         // ãåìùå
{
  int X1;                                                   // 26/03/12
  int Y1;
  int X2;
  int Y2;

  void operator = (int value)
  {
    X1 = value;  Y1 = value;
    X2 = value;  Y2 = value;
  }

  FRAME operator = (FRAME& frame)
  {
    X1 = frame.X1;  Y1 = frame.Y1;
    X2 = frame.X2;  Y2 = frame.Y2;  return *this;
  }

  int operator != (int value)
  {
    return ( X1 != value || Y1 != value ||
             X2 != value || Y2 != value );
  }

  int operator == (int value)
  {
    return ( X1 == value && Y1 == value &&
             X2 == value && Y2 == value);
  }
}
  FRAME;


